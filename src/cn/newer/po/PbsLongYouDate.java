package cn.newer.po;

public class PbsLongYouDate {
	private String name;
	private String number;
	private String lon;
	private String lat;
	private String rentcount;
	private String restorecount;
	
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	public String getLon() {
		return lon;
	}
	public void setLon(String lon) {
		this.lon = lon;
	}
	public String getLat() {
		return lat;
	}
	public void setLat(String lat) {
		this.lat = lat;
	}
	public String getRentcount() {
		return rentcount;
	}
	public void setRentcount(String rentcount) {
		this.rentcount = rentcount;
	}
	public String getRestorecount() {
		return restorecount;
	}
	public void setRestorecount(String restorecount) {
		this.restorecount = restorecount;
	}
	
	
	
}
